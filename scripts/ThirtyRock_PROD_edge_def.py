def return_reverselookup():

    ipg_db = {}

    for room, parts in ROOM_COLLECTION.items():

        for group, value in parts.items():

            if isinstance(value, list):

                for ipg in value:
                    ipg_db.update({ipg: {"PCR": room}})

            else:

                switcher_list = eval("{}['{}']".format(group, value))

                for ipg in switcher_list:
                    ipg_db.update({ipg: {"PCR": room, "SWITCHER": value}})

    return ipg_db


